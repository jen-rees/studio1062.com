                <h2>City Administration</h2>
    <p>The City admin is comprised of two individuals. The City Manager, Dan Berman (second from the left) and the City Clerk, Gabriel Adams (far left).</p>
    <p>The City Manager acts as the Chief Executive Officer of the City.  The Manager is selected by the Council and serves at the pleasure of the Council.  The Manager oversees the functions of the City and its employees on a daily basis.  In working with the City’s elected officials, the Manager is also responsible for preparing for the monthly meetings of the City Council.</p>
    
    <p>In Trinidad, due to the small size of the City and its staff, the City Manager has two additional functions that are typically held by other professionals in larger cities.  First, the Manger acts as Trinidad’s Finance Officer and is responsible for managing the preparation of the annual budget, the annual audit, ongoing accounts payable and receivable, employee payroll and benefits, regional/state/federal grant programs, and City investments.  Second, the Manager coordinates the activities of the contract consultants that handle other City functions where there are no full-time employees.  These include planning and building, legal services, engineering, and accounting.</p>
    
  
    
    <p>The City Clerk handles varied functions in providing services to the City Council and the residents of Trinidad.  The Clerk acts as the Secretary to the City Council and to the City’s Planning Commission.  In doing so, the Clerk maintains all official City records that include the records of public meetings, the official actions of the City Council in the form or Minutes, Proclamations, Resolutions, and Ordinances.</p>
    
    <p>The Clerk’s office is the hub for providing services to the City residents.  City licenses and permits, billing for water and the payment of water bills, the coordination of the Trinidad Cemetery, Town Hall use and rental, local recycling programs, and the general response to citizen inquiries all originate at the Clerk’s office in Town Hall.</p>
    
